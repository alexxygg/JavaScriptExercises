const mathTools = {
  addition(x, y) {
    return x + y;
  },
  substraction(x, y) {
    return x - y;
  },
  multiplication(x, y) {
    return x * y;
  },
  division(x, y) {
    return x / y;
  },
  squaredNum(number) {
    return number * number;
  },
  cubedNum(number) {
    return number * number * number;
  },
};
